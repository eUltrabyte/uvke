    Interface::~Interface() {
        ImGui_ImplVulkan_DestroyFontsTexture();
        
        vkDestroyDescriptorPool(m_base->GetDevice(), m_descriptorPool, nullptr);
        ImGui_ImplVulkan_Shutdown();

        UVKE_LOG_ADDRESS("Interface Destroyed");
    }

    void Interface::Render(CommandBuffer* commandBuffer, unsigned int frame) {
        ImGui_ImplVulkan_NewFrame();
        ImGui_ImplGlfw_NewFrame();
        ImGui::NewFrame();

        ImGui::Begin("Stats");

        ImGui::Text("Render Time - %.4fms", m_stats.renderTime);
        ImGui::Text("Frame Per Second - %i", m_stats.fps);
        ImGui::Text("GPU - %s", m_base->GetPhysicalDeviceProperties().deviceName);
        ImGui::Text("Vulkan - %u.%u.%u", VK_API_VERSION_MAJOR(m_base->GetPhysicalDeviceProperties().apiVersion), VK_API_VERSION_MINOR(m_base->GetPhysicalDeviceProperties().apiVersion), VK_API_VERSION_PATCH(m_base->GetPhysicalDeviceProperties().apiVersion));
        ImGui::Text("Driver - %u.%u.%u", VK_API_VERSION_MAJOR(m_base->GetPhysicalDeviceProperties().driverVersion), VK_API_VERSION_MINOR(m_base->GetPhysicalDeviceProperties().driverVersion), VK_API_VERSION_PATCH(m_base->GetPhysicalDeviceProperties().driverVersion));
        ImGui::Text("Position - %f/%f/%f", m_stats.position.x, m_stats.position.y, m_stats.position.z);
        ImGui::End();

        ImGui::Render();
        ImGui_ImplVulkan_RenderDrawData(ImGui::GetDrawData(), commandBuffer->GetCommandBuffer(frame));
    }

    void Interface::SetStats(Stats stats) {
        m_stats = stats;
    }
